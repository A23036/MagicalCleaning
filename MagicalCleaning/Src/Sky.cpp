#include "Sky.h"

// ---------------------------------------------------------------------------
// コンストラクタ
// ---------------------------------------------------------------------------
Sky::Sky()
{
	mesh = new CFbxMesh();
	mesh->Load("data/Map2/sky2.mesh");
	SetPosition(0, 0, 0);
	rotate = 0.0f;
}

// ---------------------------------------------------------------------------
// デストラクタ
// ---------------------------------------------------------------------------
Sky::~Sky()
{
	SAFE_DELETE(mesh);
}

// ---------------------------------------------------------------------------
// SkyBoxの更新処理
// 
// ゆっくりと回転させる
// ---------------------------------------------------------------------------
void Sky::Update()
{
	SetRotation(0, rotate * DegToRad, 0);
	rotate += 0.01;
}

// ---------------------------------------------------------------------------
// SkyBoxの描画処理
// 
// ライティングを反映させずに描画する
// ---------------------------------------------------------------------------
void Sky::Draw()
{
	VECTOR3  prevVec = GameDevice()->m_vLightDir;
	GameDevice()->m_vLightDir = VECTOR3(0, 0, 0);
	Object3D::Draw();
	GameDevice()->m_vLightDir = prevVec;
}
